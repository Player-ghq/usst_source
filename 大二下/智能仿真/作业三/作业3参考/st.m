% --- Executes on button press in pushbutton4.
function pushbutton4_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton4 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
year = str2num(get(handles.edit2,'string'));
cdate = handles.cdate;
fit = handles.fit;
e = handles.e;
sum=0;
if ismember(year,1790:1990)
    syear = (year - mean(cdate))/std(cdate);
    for i = 1:e+1
        sum = sum + fit(i)*(syear^(e-i+1));
        set(handles.edit3,'string',num2str(sum));
    end
else
    set(handles.edit3,'string','请输入范围内的年份！');
end


