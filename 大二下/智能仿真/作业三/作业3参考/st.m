% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
cdate = handles.cdate;
pop = handles.pop;
e = str2num(get(handles.edit1,'string'));
sdate = (cdate - mean(cdate))./std(cdate);
fit = polyfit(sdate,pop,e);
fitpop = polyval(fit,sdate);
res = pop - fitpop;
handles.e = e;
handles.fit = fit;
guidata(hObject,handles);
cla reset;
hold on;
plot(cdate,pop,'r*-');
plot(cdate,fitpop,'b*-');
plot(cdate,res,'k*-');
grid on;
legend('原始数据','拟合数据','残差');
str = char(vpa(poly2sym(fit),3));
str = ['y = ' str];
set(handles.edit4,'string',str);


